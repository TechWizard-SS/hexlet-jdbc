package patterns.singletones;

/**
 * Реализация паттерна Singleton с жадной (eager) инициализацией.
 *
 * <p>Экземпляр создаётся при загрузке класса (статическая инициализация).
 * Эта реализация потокобезопасна "из коробки", так как JVM гарантирует,
 * что статические поля инициализируются только один раз и в потокобезопасном режиме.
 *
 * <h2>Плюсы:</h2>
 * <ul>
 *   <li>Потокобезопасна без дополнительных усилий.</li>
 *   <li>Простота реализации.</li>
 * </ul>
 *
 * <h2>Минусы:</h2>
 * <ul>
 *   <li>Объект создаётся сразу при запуске, даже если он не понадобится (не ленивый подход).</li>
 * </ul>
 *
 * <h2>Когда использовать:</h2>
 * Когда стоимость создания экземпляра невелика, и вы уверены, что он почти всегда будет нужен.
 */
public class SimpleSingleton {
    private final static SimpleSingleton INSTANCE = new SimpleSingleton();

    private SimpleSingleton() {}

    public static SimpleSingleton getInstance() {
        return INSTANCE;
    }
}
