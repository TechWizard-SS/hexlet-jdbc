package patterns.singletones;

/**
 * Реализация паттерна Singleton с использованием перечисления (enum).
 *
 * <p>Это самый лаконичный, безопасный и надёжный способ реализации Singleton в Java.
 * Автоматически обеспечивает:
 * <ul>
 *   <li>Потокобезопасность.</li>
 *   <li>Ленивую загрузку (на практике — при первом обращении к enum).</li>
 *   <li>Защиту от рефлексии и сериализации (в отличие от обычных классов).</li>
 * </ul>
 *
 * <h2>Плюсы:</h2>
 * <ul>
 *   <li>Гарантированно один экземпляр.</li>
 *   <li>Минимум кода.</li>
 *   <li>Устойчив к атакам через сериализацию и рефлексию.</li>
 * </ul>
 *
 * <h2>Минусы:</h2>
 * <ul>
 *   <li>Нельзя наследовать от других классов (enum наследуется от {@link java.lang.Enum}).</li>
 *   <li>Менее гибкий синтаксис, если нужны сложные зависимости или инициализация.</li>
 * </ul>
 *
 * <h2>Когда использовать:</h2>
 * Почти всегда, если у вас нет специфических требований. Рекомендован Джошуа Блохом в "Effective Java".
 */
public enum EnumSingleton {
    INSTANCE;

    // Можно добавлять методы и поля, как в обычном классе
    public void doSomething() {
        // ...
    }
}